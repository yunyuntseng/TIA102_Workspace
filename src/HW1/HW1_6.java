package HW1;

public class HW1_6 {
	public static void main(String[] args) {
		System.out.println(5+5); // 5+5為數字相加，結果為10。
		System.out.println(5+'5'); // '5'為字元字面常數(char)，用單引號表示。字元值定數代表一個Unicode字元，其編號以十六進位表示。數字5在unicode邊碼表中為0035，換算
		System.out.println(5+"5"); // "5"為字串(String)，用雙引號表示。數字+字串會串接，不會相加，故結果為55。
	}

}
